      var $current_cmt = $(this).parents('.cmt_item');
      $current_cmt.find('.cmt_tool_area').removeClass('on');
      var liitem = '<li class="cmt_item_reply"></li>';
      $current_cmt.after(liitem);
      $current_cmt.next('.cmt_item_reply').html(reply_html);
    }).on('click', '.btn_remove_cmt', function() {//댓글 삭제하기
        var $current_cmt = $(this).parents('.cmt_item, .cmt_item_reply');
        $current_cmt.remove();        
    }).on('click','.btn_apply_cmt', function(){//댓글 등록하기
        var cmt_val = $(this).parents('.cmt_area').children('.cmt_txt').val();
        var $cmt_container = $('.cmt_container ul');
        $cmt_container.append(cmt_html);
        var cmt_input = $cmt_container.find('.cmt_item').last().find('.cmt_text');
        cmt_input.text(cmt_val);
    }).on('click','.count_info', function(){
        $(this).toggleClass('open');
